#!/bin/sh
set -e
set -o noglob

IMAGES_TXT=images.txt
SAVE_FILE_NAME=save-images
IMAGES_ARR=()
TXT_ARR=()
help=false

usage () {
  echo "USAGE: $0 [--txt rancher-images.txt] [--images registry:3.1.1] [--out rancher-images.tar.gz]"
  echo "  [-t|--txt url] txt file with list of images; one image per line."
  echo "  [-i|--images image] docker image full name."
  echo "  [-o|--out file] tar.gz generated by docker save."
  echo "  [-h|--help] Usage message"
}

POSITIONAL=()
while [[ $# -gt 0 ]]; do
  key="$1"
  case $key in
    -t|--txt)
    TXT_ARR+=($2)
    shift # past argument
    shift # past value
    ;;
    -i|--images)
    IMAGES_ARR+=($2)
    shift # past argument
    shift # past value
    ;;
    -o|--out)
    SAVE_FILE_NAME=$2
    shift # past argument
    shift # past value
    ;;
    -h|--help)
    help=true
    shift
    ;;
    *)
    usage
    exit 1
    ;;
  esac
done

if [[ $help ]]; then
  usage
  exit 0
fi
