#!/bin/sh
set -e
set -o noglob
########################################################################
### 生成离线镜像包
# $ curl -sfL https://renlm.github.io/sh/docker-save-images.sh | \
#     PLATFORM=linux/amd64 sh -s - \
#     -t https://renlm.github.io/resources/cert-manager/v1.20.2/images.txt \
#     -t https://github.com/rancher/rancher/releases/download/v2.14.2/rancher-images.txt \
#     -i registry:3.1.1 \
#     -i nginx:1.31.2-alpine \
#     -o rancher-images-v2.14.2
########################################################################
IMAGES_TXT=images.txt
OUTPUT=save-images
IMAGES_ARR=""
TXT_ARR=""
PULLED=""
help=false
# linux/amd64
# linux/arm64
PLATFORM=${PLATFORM}

# 颜色代码
_RED_='\033[0;31m'    # 红色
_GREEN_='\033[0;32m'  # 绿色
_YELLOW_='\033[0;33m' # 黄色
_NC_='\033[0m'        # 重置

# --- helper functions for logs ---
info()
{
  printf "[ ${_GREEN_}INFO${_NC_} ] $@\n"
}
warn()
{
  printf "[ ${_YELLOW_}WARN${_NC_} ] $@\n" >&2
}
fatal()
{
  printf "[ ${_RED_}ERROR${_NC_} ] $@\n" >&2
  exit 1
}

# 参数校验
if [ "$PLATFORM" = "linux/amd64" ] || [ "$PLATFORM" = "linux/arm64" ]; then
  info "PLATFORM: $PLATFORM"
else
  fatal "Unknown PLATFORM: $PLATFORM, linux/amd64 or linux/arm64"
  exit 1
fi

# --- download from url ---
download() {
  [ $# -eq 2 ] || fatal 'download needs exactly 2 arguments'

  # Disable exit-on-error so we can do custom error messages on failure
  set +e

  # Default to a failure status
  status=1

  case $DOWNLOADER in
    curl)
      printf "[ ${_GREEN_}下载${_NC_} ] curl -o $1 -sfL $2\n"
      curl -o $1 -sfL $2
      status=$?
    ;;
    wget)
      printf "[ ${_GREEN_}下载${_NC_} ] wget -qO $1 $2\n"
      wget -qO $1 $2
      status=$?
    ;;
    *)
      # Enable exit-on-error for fatal to execute
      set -e
      fatal "Incorrect executable '$DOWNLOADER'"
    ;;
  esac

  # Re-enable exit-on-error
  set -e

  # Abort if download command failed
  [ $status -eq 0 ] || fatal 'Download failed'
}

usage () {
  info "USAGE: $0 [--txt rancher-images.txt] [--images registry:3.1.1] [--output rancher-images.tar]"
  info "  [-t|--txt url] txt file with list of images; one image per line."
  info "  [-i|--images image] docker image full name."
  info "  [-o|--output fileName] tar generated by docker save."
  info "  [-h|--help] Usage message"
}

while [ $# -gt 0 ]; do
  key="$1"
  case $key in
    -t|--txt)
    TXT_ARR="$TXT_ARR $2"
    shift # past argument
    shift # past value
    ;;
    -i|--images)
    IMAGES_ARR="$IMAGES_ARR $2"
    shift # past argument
    shift # past value
    ;;
    -o|--output)
    OUTPUT=$2
    shift # past argument
    shift # past value
    ;;
    -h|--help)
    help=true
    shift
    ;;
    *)
    usage
    exit 1
    ;;
  esac
done

if $help; then
  usage
  exit 0
fi

# 下载镜像
DOWNLOADER=curl
rm -fr ./${OUTPUT}
mkdir ${OUTPUT}
touch ${OUTPUT}/${IMAGES_TXT}
docker_pull() {
  if [ ! -z "$@" ]; then
    echo "$@" >> ${OUTPUT}/${IMAGES_TXT}
    if docker pull --platform ${PLATFORM} "$@" > /dev/null 2>&1; then
      info "Image pull success: $@"
      PULLED="${PULLED} $@"
    else
      if docker inspect "$@" > /dev/null 2>&1; then
        PULLED="${PULLED} $@"
      else
        fatal "Image pull failed: $@"
      fi
    fi
  fi
}
for image in $IMAGES_ARR; do
  docker_pull $image
done
for txt in $TXT_ARR; do
  txt_file=${txt##*/}
  download $txt_file $txt
  sed -i '/^#/d' $txt_file
  while IFS= read -r line; do
    docker_pull $line
  done < $txt_file
done

IMAGES_NUM=$(echo ${PULLED} | wc -w | tr -d '[:space:]')
info "Creating ${OUTPUT}.tar.gz with $IMAGES_NUM images"
docker save -o ${OUTPUT}/${OUTPUT}.tar $PULLED
tar -czf ${OUTPUT}.tar.gz ${OUTPUT}
